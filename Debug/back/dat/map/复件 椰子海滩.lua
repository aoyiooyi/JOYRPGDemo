function ChangeLocation(scene)

	------------- 释放旧的场景资源 -----------------
	scene:FreeMap();            -- 释放地图资源
	scene:ClearAnimations();    -- 释放animation资源

	------------- 读取新的场景资源 -----------------
	scene:LoadMap(backGroundImg,backGroundMaskImg,       -- 读取地图资源
		gridWidth,gridHeight,width,height);

	------------- 删除旧的传送点 -------------------
	scene:ClearPortal();


	-------------- 添加传送点 --------------------
	scene:AddPortal(1200, 100, "./dat/map/克兰村1.lua");
	--scene:AddPortal(400, 400, "./dat/map/克兰村1.lua");
	scene:AddPortal(700, 600, "./dat/map/长寿村.lua");

	-------------- 添加怪物资源 -------------------
	local gameGlobalInfo = GetGameGlobalInfo();
	for i = 1, monsterNum, 1 do
		gameGlobalInfo:AddNativeMonsterDataPath(monsters[i]);
	end


	--释放资源管理器的所有资源
	scene:ClearRscManager();

	--初始化场景资源管理器
	scene:InitRscManager(anmNum);

	--读取每一种animation资源
	for i = 1,anmNum,1 do

		index[i] = scene:LoadAnimation(texPath[i], frams[i], fps[i], tx[i], ty[i], w[i], h[i]);

	end

	-- 给资源索引赋值
	--PortalIndex = index[1];

	--添加资源实体
	for i = 1,PortalNum,1 do

		scene:AddAnimation(PortalIndex, PortalX[i], PortalY[i]);

	end


	--设置玩家起始位置
	scene:SetPlayerPoint(playerX, playerY);

	scene:AddNPC(SellerPath, sellerX, sellerY, sellerWidth, sellerHeight, sellerFrame, sellerFPS);

end
